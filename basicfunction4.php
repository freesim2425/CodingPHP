<?PHP
// ฟังก์ชั่นแบบรับค่าเข้า และ ส่งค่าออก
function getTotal($salary,$count){ // รับ parameter 
    // คำนวนเงินเดือน * จำนวนเดือน
    return $salary * $count;
}
// เรียกใช้ 
print(getTotal(15000,3));
// ส่ง อากิวเมนต์ 15000 จะถูกส่งไปเก็บไว้ในตัวแปร $salary และ 3 จะถูกส่งไปเก็บไว้ที่ $count ตามลำกับเพื่อนำไปคำวนวนต่อ
?>